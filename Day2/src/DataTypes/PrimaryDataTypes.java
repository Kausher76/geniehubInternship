package DataTypes;

public class PrimaryDataTypes {
	
	public static void main(String[] args) {
		
		//numbers
		byte myByte = 35; //1 byte,  -128 to 127 
		short myShort = 355; // 2 bytes, -32,768 to 32,767
		int  myInt = 76778; // 4 bytes,  -2,147,483,648 to 2,147,483,647
		long myLong = 784734434L; // 8 bytes, -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		float myFloat = 65.7f; // 4 bytes, Sufficient for storing 6 to 7 decimal digits
		double myDouble = 76.87732d; // 8 bytes, Sufficient for storing 15 decimal digits
		
		
		boolean isPrime = true;
		char Mychar = 'c';
		
		
		
		
		
	}
}
